                        <h1>We Banter -Újratöltve</h2>
                            <h5>Szerző: VaszilijEDC Ultra | 2025. ápr. 20. | Novella</h5>
                            <br>
                            4 évvel ezelőtt született egy cikk Ben Petersen első saját tervezésű késéről (We Banter).
                            Most, hogy megjelent a második generáció, valamint hogy lassan másfél éve használok egy
                            módosított verziót az
                            első szériából, elegendő oknak bizonyult, hogy újra górcső alá kerüljön a típus. Lássuk, mi
                            változott, megugorja vajon az első változat által magasra tett lécet az új rokon?<br><br>

                            WE modBanter <br>

                            A customizált verzió az egyik késes csoport “Karácsonyi karika” elnevezésű játéka során
                            került a tulajdonomba. Eredeti gazdája néhány olyan ponton módosított rajta a gyári
                            változathoz képest, amik a felhasználói élményt
                            nagyban képesek befolyásolni. Több éltárs is meglepődött, amikor kézbe fogta ezt az egyedi
                            darabot. Bár majd mindenki találkozott az eredeti verzióval, az nem aratott akkora sikert,
                            mint ez a egyediség.
                            <br>
                            Nézzük sorban a módosításokat.
                            <br>
                            <br>
                            1. Szín
                            <br>
                            Az eredetileg szürke markolatpanel egy speciális festékkel lila színűvé változott. Mélyen a
                            G10 anyagába diffundálva garantálja, hogy nem fog kopni vagy fakulni a használat során a kés
                            színe. Mindenhol egységes,
                            átmenet és folt nélkül sikerült a művelet. Ha nem tudnám, hogy ilyen színben sohasem volt
                            elérhető a Banter, akár gyári verziónak is hihetném.
                            <br><br>
                            2. Furat
                            <br>
                            A show markolatpanelen elsőre feltűnik, hogy egy 8 mm-es furat fedi fel a távtartók közt
                            megbúvó penge egy részét. Túl azon, hogy szerintem jól néz ki, van egy nagyon egyszerű
                            gyakorlati haszna is. A zsebből
                            kivéve ugyanis a G10 panel a szoros klipsszel együtt hajlamos volt nehézkessé tenni ezt az
                            egyszerű mozdulatot. A furat ezt segít kiküszöbölni: a hüvelykujjam beleakasztva kényelmesen
                            és csúszás nélkül tudom
                            azonnal elővenni a modBantert a zsebemből. Mivel pár késnél találkoztam ezzel a megoldással,
                            (Spyderco Watu, Fox Suru, Real Steel Bruns) örömmel fedeztem fel itt is.
                            <br><br>
                            3. Ujjvédő
                            <br>
                            A gyári verzióhoz képest az íves panelvég és alatta a penge lecsiszolásra került, így kb.
                            egy centivel közelebb lehet fogni a penge tövéhez. Nekem ez volt az a módosítás, ami a
                            legtöbbet tett azért, hogy
                            nemcsak jól használható, hanem állandó zseblakóvá vált a különc WE. A gyári verzió ugyanis a
                            vásárlást követő két hét után a szekrényben landolva pihenésre lett kárhoztatva. Nem bírtam
                            megszokni, hogy az íves rész elvesz annyi hasznos helyet a markolatból, hogy ezáltal
                            kényelmetlen,
                            túlságosan rövid számomra. A módosítást követően végre négyujjassá vált a kés, a finomabb
                            munkákban könnyebben irányítható a penge, a fogása pedig egy cseppet sem lett veszélyesebb
                            az ujjvédő hiánya miatt.
                            <br><br>
                            4. Molettázás
                            <br>
                            Sajnos erre a módosításra nem találtam egy az egyben megfelelőt a magyar nyelvben, így el
                            kell fogadnotok az általam kitalált szót. A forma miatt “Moletta”, alias Michele Pensato
                            jutott eszembe. Ő
                            ugyanis előszeretettel alkalmaz ilyesfajta megjelenést az általa tervezett késeken. A penge
                            alakja egy kisebb beavatkozásnak köszönhetően hosszban ugyan nem változott, de “molettásabb”
                            kinézetet kapott.
                            Tömzsibb megjelenésűvé vált, a hegy még kevésbé hajlamos a sérülésre, illetve a szúráskor
                            fellépő ellenállás is talán kisebb a beavatkozás után.
                            <br><br>
                            5. Finis
                            <br>
                            A penge és a klipsz is kapott (már tőlem) egy házi kőmosást. A szatén pengefinis annyira nem
                            az én világom, hogy a használat során keletkező harci sérülések minden egyes késemnél
                            egy-egy gyomorszájra
                            bevitt ütéssel érnek fel. Ezt megelőzve kőmostam és újra éleztem a Banter S35vn pengéjét.
                            Számomra így már a megjelenés mellett a használat is első osztályúvá vált.
                            <br><br>
                            Milyen a Banter, ha a személyi száma kettes?
                            <br><br>
                            Ben Petersen új Bantere bár kinézetre másképp mutat, alapvetően ugyanazt a szerepet hivatott
                            betölteni, amit az első verzió. Egy mindenki számára kényelmesen hordható és használható,
                            kiváló minőségű és
                            megfizethető folder kíván lenni. A régi, faék egyszerűségű körvonal megmaradt ugyan, de már
                            sokkal finomabb részletekkel kényezteti a késbarátokat.
                            <br><br>
                            Műszaki adatok:
                            <br>
                            Penge hossza: 73 mm
                            <br>
                            Penge anyaga: CPM S35VN
                            <br>
                            Penge vastagsága: 2,6 mm
                            <br>
                            Penge formája: Drop point
                            <br>
                            Markolat hossza: 96 mm
                            <br>
                            Markolat anyaga: Alumínium
                            <br>
                            Zár: Crossbar Lock
                            <br><br>
                            A második generáció már nem különböző színű G10-es és fa markolatú panelekkel érhető el,
                            hanem jelenleg micarta és anodizált, mart alupanelekkel. A három féle színhez 3 különböző
                            pengefinis tartozik:
                            <br><br>
                            Zöld micarta – kőmosott felület
                            <br>
                            Kék anodizált alumínium – szatén felület
                            <br>
                            Lila anodizált alumínium – fekete kőmosott felület
                            <br><br>
                            A lila anodizált, mart alupanel nagyon jól harmonizál a fekete kőmosott felülettel.
                            Hasonlóan feketére felületkezelték a crossbar lock nyitógombjait, a penge nyitószemölcseit,
                            a klipszet és a csavarokat is.
                            <br><br>
                            A markolatpanel mart barázdái kellemes hűvösséggel simulnak a kézbe, nagyon jó tapadást
                            biztosítva. Hordás közben sajnálattal tapasztaltam, hogy hajlamos a kopásra, így akit ez
                            zavar, gondolja meg ezen típusok
                            vásárlását. A hossza is változott az első kiadáshoz képest. Mindössze 5 mm-el nőtt meg, de
                            ez előnyévé válik a késnek. A lekerekítése pont annyira íves, hogy sokkal kényelmesebb
                            használni, mint idősebb társát.
                            <br><br>
                            A két markolatpanel közti traktusban találhatóak a legnagyobb különbségek az első verzióhoz
                            képest. A panelek alatt könnyített linerek vannak, immár a zár miatt mindkét oldalon.
                            <br><br>
                            A második a zár típusa. A WE is felült a keresztzár vonatra, ami manapság nagyon népszerű a
                            késes körökben. Kijelenthetjük, hogy nemcsak meglovagolja ezt a sikerességet, hanem nevéhez
                            méltóan szinte tökéletesre
                            fejlesztette saját crossbar rendszerét. A piacon található egyik – ha nem a legjobban működő
                            – keresztzárja ugyanis olyan szintű fidget faktorral rendelkezik, hogy az folyamatos játékra
                            csábítja a gyanútlan késfelhasználót.
                            Késbaráti társaságban körbeadva elképedő arcokat szült a Banter2, nem győztem felszedni a
                            leesett állakat a földről. Mindenki szuperlatívuszokban beszélt róla, pedig a Benchmade
                            szabadalmaztatta zártípus bemutatása óta jó pár
                            gyártó saját verzióját kipróbálhatta a nagyérdemű. Ez viszont láthatóan nagyon betalált az
                            érdeklődőknél.
                            <br><br>
                            A harmadik jól látható és hasznos különbség, hogy távtartók helyett egy, a markolattal
                            színben harmonizáló backspacer került beépítésre. Szerepe leginkább a kés stabilizálása.
                            Szerencsére a WE minőségre olyan jó
                            példa, hogy nem igazán tűnik fel, annyira sikerült integrálni.
                            <br><br>
                            A markolatpanel klipsszel ellentétes oldalán egy, a pengével színben megegyező takarólemez
                            vidítja fel a késes közösség balkezes tagjait. Ugyanis végre megfordítható lett a klipsz,
                            így igazán kétkezessé vált Ben
                            Petersen kése. Süllyesztett csavarjai nagy piros pontot érdemelnek, mondhatjuk: csak a
                            szokásos WE minőség.
                            <br><br>
                            Az első generációhoz képest a logózott tengelycsavar nem olyan hangsúlyos, nekem jobban is
                            tetszik.
                            <br><br>
                            A penge maradt a már jól bevált S35vn porkohászati acél, drop point formájú. Jelen esetben
                            fekete kőmosottat kapunk, ami a lilával nagyon jó kontrasztot mutatva kellemes megjelenésűvé
                            teszi alanyunkat. Továbbra is
                            elmondható, hogy ez az acéltípus jól ellenáll a korróziónak, hosszan tartja az élét és házi
                            körülmények közt is egész könnyen karbantartható. A rajta található jelzések megegyeznek az
                            előző kiadásnál már megszokottakkal,
                            úgymint csak a jobb oldalon látható diszkrét anyagjelölés és Ben Petersen jól ismert logója.
                            Kerámia golyóscsapágyakon a penge a közhelyes “ zsírozott villám “ effektust bemutatva repül
                            ki a helyéről, záráskor hasonlóan csukódik.
                            <br><br>
                            A rácsúszásgátló recézése kicsit durvább és a hossza pont a duplája a réginek, valamint a
                            sharpening choil is megnőtt.
                            <br><br>
                            Használat közben
                            <br><br>
                            Sajnos sok újdonságot nem nagyon fogok tudni veletek megosztani. Amennyiben eltekintünk a
                            penge 73 mm-es hosszától, egész jól használható a mindennapokban előforduló általános
                            feladatokra. A relatív vékony penge teljes
                            leélezése, pengegeometriája és a WE-től megszokott tökéletes élszalag kombinációja nagyon jó
                            minőségű vágást tesz lehetővé. A szokásos konyhai munkák, a kartonvágás és étkezés után
                            semmi nyoma a pengén a használatnak, ami jó jel.
                            <br><br>
                            Kézben fogva semmilyen kellemetlen nyomás vagy diszkomfort érzés nincs. Erősebb munkáknak
                            még nem tettem ki, de az idő javulásával túrázásokkor is szándékomban áll bevetni majd.
                            <br><br>
                            Konklúzió
                            <br><br>
                            Hordása a deep carry klipsznek köszönhetően teljesen észrevétlenné teszi a Banter2-t.
                            <br><br>
                            Jó hír, hogy a penge fekete finise nálam eddig kopást még nem mutat, ellentétben az
                            anodizált markolatú alumínium panellel. Könnyű és továbbra is szinte észrevétlen a zsebben,
                            bár az új generáció már egyáltalán nem az a
                            faék, aminek sokan tartották és tartják az előzőt. A kifogástalan zár és a felhasználói
                            visszajelzések alapján megváltoztatott apróságok mind a vásárlók igényeinek kiszolgálását
                            mutatja. Nyitása és csukása példaértékű,
                            akár oktatóanyag is lehetne a késkészítő nagykönyvben. A penge semmilyen irányban nem kotyog
                            vagy lötyög: teljesen stabilan áll, akár zárt, akár csukott állapotban van. Az általános
                            minőségre nem nagyon lehet panasz,
                            úgy vélem a pénzünkért egy kimondottan kellemes, immár sokkal kiforrottabb foldert kapunk,
                            ami minden tekintetben felülmúlja elődjét. A Banter2 a nevéhez méltóan ugratja a
                            versenytársakat, fricskát mutatva nekik az
                            ár-érték-fidget faktor háromszögben.
