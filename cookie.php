                <h5>A cookie (süti)</h5><br>
                A cookie vagy süti jelentése: file-ok vagy információ darabkák, amelyeket internetes böngészője ment le
                honlapunkról és tárolja azokat az Ön gépén. Ezeknek a cookie-knak a segítségével ismeri fel a honlapunk
                anyagait tároló szerver számítógép a honlapunkra történő visszalátogatásakor, hogy Ön már járt a
                honlapomon.

                A legtöbb internetes böngésző alapbeállításából kifolyólag elfogad cookie-kat. Ha úgy gondolja,
                átállíthatja böngészőjét, hogy a cookie-kat visszautasítsa vagy figyelmeztesse Önt arra, hogy cookie-kat
                küldtek az Ön gépére. Weboldalunk használ ilyen cookie fájlokat, egyes funkciók biztosítása érdekében
                vagy csupán kényelmi okok miatt. Az általunk használt cookie-k nem terhelik, lassítják és nem okoznak
                kárt az Ön számítógépében.

                Az oldalon Harmadik féltől származó cookie-k, sütik használata is történik. A cookie-k, sütit
                törölhetőek a böngészőkből, továbbá le is tilthatóak a sütik, cookie-k. Ezen beállításairól a böngésző
                hivatalos weboldalán talál információt.
                <br><br>

                <h5>Mi a cookie-k célja?</h5><br>
                Többféle célra is használjuk ezeket a technológiákat, például a Felhasználó számára legrelevánsabb
                tartalom vagy hirdetés megjelenítésére, termékeink és szolgáltatásaink továbbfejlesztésére, valamint
                szolgáltatásaink biztonságának megőrzésére. Az általunk használt cookie-k, képpontok és egyéb hasonló
                technológiák pontos neve a szolgáltatások fejlesztésével és frissítésével időről-időre változhat.
                <br><br>

                <h5>Hogyan keletkezik a cookie?</h5><br>
                Először a kliens gép küld egy kérést a kiszolgáló irányába. Ekkor a kiszolgáló létrehoz egy egyedi
                azonosítót és ezt eltárolja a saját adatbázisában, majd az így létrehozott cookie-t az összes
                információval visszaküldi a kliensnek. Az így visszakapott információs cookie eltárolódik a kliens
                gépen.

                <br><br>

                <h5>Hogyan használódik a cookie?</h5><br>
                Amikor a kliens gép újra kapcsolatba lép a kiszolgálóval már hozzácsatolja az előzőleg már létrehozott
                és eltárolt cookie-t. A kiszolgáló összehasonlítja a kapott és az általa tárolt cookie tartalmát.
                Ezáltal könnyedén azonosítja pl. a regisztrált vagy visszalátogató felhasználót.
                <br><br>

                <h5>Milyen cookie-t használunk?</h5><br>
                Többféle cookie van használatban, de minden weboldal másféle típusúakat használ. Honlapunk általában
                csak az alábbiakat használja, de a fejlesztések alkalmával előfordulhat, hogy az eddigieken felül újabb
                fajtákat is alkalmazunk.
                <br><br>

                <h5>Munkameneti/Átmeneti cookie:</h5><br>
                Ezek a cookie-k csak átmenetileg kerülnek az ideiglenes memóriába, ameddig a felhasználó az oldalon
                navigál. Amikor a felhasználó bezárja a böngészőt a cookie törlődik. Ezek a cookie-k nem tartalmaznak
                személyes adatokat és nem alkalmasak arra, hogy a látogatót beazonosítsa.
                <br><br>

